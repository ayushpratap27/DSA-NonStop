void f(string &tiles, vector<bool> &vis, string str, unordered_set<string> &st){
    if(str.length() != 0){
        st.insert(str);
    }

    for(int i = 0; i < tiles.size(); i++) {
        if(!vis[i]){
            vis[i] = true;
            f(tiles, vis, str + tiles[i], st);
            vis[i] = false;
        }
    }
}

int numTilePossibilities(string tiles) {
    int n = tiles.size();
    unordered_set<string> st;
    vector<bool> vis(n, false);
    f(tiles, vis, "", st);

    reverse(tiles.begin(), tiles.end());
    f(tiles, vis, "", st);

    return st.size();
}