void f(string &str, int n, int &k, string &ans){
    if(str.size() == n){
        k--;
        if(k == 0){
            ans = str;
        }
        return;
    }

    for(char ch = 'a'; ch <= 'c'; ch++){
        if(str.empty() || str.back() != ch){
            str.push_back(ch);
            f(str, n, k, ans);
            str.pop_back();

            if(k == 0) return;
        }
    }
}

string getHappyString(int n, int k) {
    string ans = "";
    string temp = "";
    
    f(temp, n, k, ans);

    return ans;
}