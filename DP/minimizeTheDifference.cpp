int solve(int i, int sum, int target, vector<vector<int>> &mat, vector<vector<int>> &dp) {
    int n = mat.size();
    int m = mat[0].size();
    if (i == n) return abs(sum - target);
    
    if (dp[i][sum] != -1) return dp[i][sum];

    int res = INT_MAX;
    for (int val : mat[i]) {
        res = min(res, solve(i + 1, sum + val, target, mat, dp));
    }

    return dp[i][sum] = res;
}

int minimizeTheDifference(vector<vector<int>>& mat, int target) {
    int n = mat.size();
    int m = mat[0].size();
    vector<vector<int>> dp(n, vector<int>(4901, -1));

    return solve(0, 0, target, mat, dp);
}