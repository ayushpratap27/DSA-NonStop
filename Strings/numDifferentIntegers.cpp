int numDifferentIntegers(string s) {
    int n = s.size();
    int i = 0;
    unordered_set<string> st;
    while(i < n){
        if(i < n && !isdigit(s[i])){
            i++;
            continue;
        }
        string temp = "";
        if(i < n && isdigit(s[i])){
            while(isdigit(s[i])){
                temp += s[i];
                i++;
            }
            int j = 0;
            while(j < temp.size() && temp[j] == '0' ){
                j++;
            }
            string trimmed = temp.substr(j);
            if(trimmed == "") trimmed = "0";
            st.insert(trimmed);
        }
    }
    return st.size();
}