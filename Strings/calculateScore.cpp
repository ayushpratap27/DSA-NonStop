char getMirror(char ch) {
    return 'a' + ('z' - ch);
}

long long calculateScore(string s) {
    int n = s.size();
    vector<stack<int>> lastIndex(26);
    long long score = 0;

    for (int i = 0; i < n; i++) {
        char mirrorChar = getMirror(s[i]);
        int mirrorIdx = mirrorChar - 'a';
        if (lastIndex[mirrorIdx].empty()) {
            lastIndex[s[i] - 'a'].push(i);
        } else {
            score += (i - lastIndex[mirrorIdx].top());
            lastIndex[mirrorIdx].pop();
        }
    }

    return score;
}