bool isPrime(int n) {
    if (n <= 1) return false;
    if (n == 2) return true;
    if (n % 2 == 0) return false;
    for (int i = 3; i * i <= n; i += 2) {
        if (n % i == 0) return false;
    }
    return true;
}

bool checkPrimeFrequency(vector<int>& nums) {
    unordered_map<int, int> mp;

    for(int i = 0; i < nums.size(); i++) {
        mp[nums[i]]++;
    }

    for(auto it : mp) {
        if(isPrime(it.second)) return true;
    }

    return false;
}