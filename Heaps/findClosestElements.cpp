vector<int> findClosestElements(vector<int>& arr, int k, int x) {
    int n = arr.size();

    priority_queue<pair<int, int>> pq;

    for(auto ele : arr) {
        pq.push({abs(x - ele), ele});
        if(pq.size() > k) pq.pop();
    }

    vector<int> ans;
    while(pq.size() > 0) {
        ans.push_back(pq.top().second);
        pq.pop();
    }

    sort(ans.begin(), ans.end());

    return ans;
}