int mod = 1e9 + 7;

int maximumProduct(vector<int>& nums, int k) {
    int n = nums.size();
    priority_queue<int, vector<int>, greater<int>> pq(nums.begin(), nums.end());

    while(k--){
        int node = pq.top();
        pq.pop();
        node++;
        pq.push(node);
    }
    
    long long ans = 1;
    while(!pq.empty()){
        ans = (ans * pq.top()) % mod;
        pq.pop();
    }

    return ans;
}