bool isVowel(char ch) {
    if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') return true;
    return false;
}

int maxVowels(string s, int k) {
    int n = s.size();
    int i = 0, j = 0;
    int cntVowel = 0;
    int maxVowel = INT_MIN;

    while(j < n) {
        if(isVowel(s[j])) cntVowel++;

        if(j - i + 1 == k) {
            maxVowel = max(maxVowel, cntVowel);
            if(isVowel(s[i])) cntVowel--;
            i++;
        }

        j++;
    }

    return maxVowel;
}