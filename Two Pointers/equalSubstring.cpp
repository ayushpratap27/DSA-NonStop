int equalSubstring(string s, string t, int maxCost) {
    int n = s.length();
    int maxLen = 0;
    int l = 0, r = 0;
    int cost = 0;

    while(r < n) {
        cost += abs(s[r] - t[r]);
        if(cost > maxCost) {
            cost -= abs(s[l] - t[l]);
            l++;
        }

        if(cost <= maxCost) maxLen = max(maxLen, r - l + 1);
        r++;
    }

    return maxLen;
}