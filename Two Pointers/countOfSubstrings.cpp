bool isVowel(char ch) {
    return ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u';
}

long long atMostK(string& word, int k) {
    int n = word.size(), l = 0, cons = 0;
    long long cnt = 0;
    unordered_map<char, int> mp;
    
    for (int r = 0; r < n; r++) {
        isVowel(word[r]) ? mp[word[r]]++ : cons++;
        
        while (mp.size() == 5 && cons > k) {
            isVowel(word[l]) ? (mp[word[l]]--, mp[word[l]] == 0 ? mp.erase(word[l]) : 0) : cons--;
            l++;
        }
        cnt += (r - l + 1);
    }
    return cnt;
}

long long countOfSubstrings(string word, int k) {
    return atMostK(word, k) - atMostK(word, k - 1);
}