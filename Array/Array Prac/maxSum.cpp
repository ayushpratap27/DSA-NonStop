int maxSum(vector<int>& nums) {
    unordered_set<int> st;
    int maxi = INT_MIN;
    bool hasPositive = false, hasZero = false;
    
    for(int num : nums){
        if(num > 0){
            st.insert(num);
            hasPositive = true;
        }else if(num == 0){
            hasZero = true;
        }else{
            maxi = max(maxi, num);
        }
    }
    
    if(hasPositive){
        return accumulate(st.begin(), st.end(), 0);
    }
    
    return hasZero ? 0 : maxi;
}