int gcd(int a, int b){
    while (b != 0) {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

long long lcm(int a, int b) {
    return (long long)a * b / gcd(a, b);
}

int maxLength(vector<int> &nums) {
    int n = nums.size();
    int maxLength = 0;

    for (int i = 0; i < n; i++) {
        long long product = 1, GCD = nums[i], LCM = nums[i];
        for (int j = i; j < n; j++) {
            if (product > LLONG_MAX / nums[j]) {
                break;
            }
            product *= nums[j];
            GCD = gcd(GCD, nums[j]);
            LCM = lcm(LCM, nums[j]);

            if (product == (long long int)GCD * LCM) {
                maxLength = max(maxLength, j - i + 1);
            }
        }
    }

    return maxLength;
}