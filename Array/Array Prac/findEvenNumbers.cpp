vector<int> findEvenNumbers(vector<int>& digits) {
    int n = digits.size();
    unordered_set<int> nums; 

    for(int i = 0; i < n; i++) {
        for(int j = 0; j < n; j++) {
            for(int k = 0; k < n; k++) {
                if(i == j || j == k || i == k) {
                    continue;
                }
                int num = digits[i] * 100 + digits[j] * 10 + digits[k];
                if(num >= 100 && num % 2 == 0) {
                    nums.insert(num);
                }
            }
        }
    }

    vector<int> res;
    for(auto it : nums) {
        res.push_back(it);
    }

    sort(res.begin(), res.end());
    
    return res;
}