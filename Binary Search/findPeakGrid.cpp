vector<int> findPeakGrid(vector<vector<int>>& mat) {
    int n = mat.size();
    int m = mat[0].size();
    int low = 0, high = m - 1;

    while(low <= high){
        int mid = low + (high - low) / 2;
        int maxRow = 0;

        for(int i = 0; i < n; i++) {
            if(mat[i][mid] > mat[maxRow][mid]) {
                maxRow = i;
            }
        }

        bool left = (mid - 1 >= 0) && (mat[maxRow][mid] < mat[maxRow][mid - 1]);
        bool right = (mid + 1 < m) && (mat[maxRow][mid] < mat[maxRow][mid + 1]);

        if (!left && !right) {
            return {maxRow, mid}; 
        } else if (left) {
            high = mid - 1;
        } else {
            low = mid + 1;  
        }
    }

    return {-1, -1}; 
}